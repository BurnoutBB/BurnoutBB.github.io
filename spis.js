function toggleSpis() {
    var trescSpisu = document.getElementById("trescSpisu");
    trescSpisu.style.display = (trescSpisu.style.display === "block") ? "none" : "block";
  }

  function hideSpis() {
    var trescSpisu = document.getElementById("trescSpisu");
    trescSpisu.style.display = "none";
  }